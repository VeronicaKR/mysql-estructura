DROP DATABASE IF EXISTS veronicaspizzerie;
CREATE DATABASE veronicaspizzerie;
USE veronicaspizzerie;
CREATE TABLE CLIENT(
    ID_CLIENT INT NOT NULL AUTO_INCREMENT,
    PRIMARY KEY(ID_CLIENT),
    NOMBRE VARCHAR(20) NOT NULL,
    APELLIDO VARCHAR(20) NOT NULL,
    DIRECCION VARCHAR(20),
    CP INT(5),
    TELEFONO INT(9)
);
CREATE TABLE LOCALITAT(
    ID_LOCALITAT INT NOT NULL AUTO_INCREMENT,
    PRIMARY KEY (ID_LOCALITAT),
    NOM VARCHAR(10),
    ID_CLIENT INT,
    FOREIGN KEY (ID_CLIENT) REFERENCES CLIENT (ID_CLIENT)
);
CREATE TABLE PROVINCIA(
    ID_PROVINCIA INT NOT NULL AUTO_INCREMENT,
    PRIMARY KEY (ID_PROVINCIA),
    NOM VARCHAR(10),
    ID_CLIENT INT,
    FOREIGN KEY (ID_CLIENT) REFERENCES CLIENT (ID_CLIENT)
);
CREATE TABLE COMANDA(
    ID_COMANDA INT NOT NULL AUTO_INCREMENT,
    PRIMARY KEY (ID_COMANDA),
    FECHA_Y_HORA DATETIME,
    ENTREGA_O_RECOGER VARCHAR(15),
    CANTIDAD_PRODUCTOS INT(20),
    PRECIO_TOTAL INT (100),
    ID_CLIENT INT,
    FOREIGN KEY (ID_CLIENT) REFERENCES CLIENT (ID_CLIENT)
);
