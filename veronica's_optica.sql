DROP PROCEDURE IF EXISTS veronicasoptica;
CREATE DATABASE veronicasoptica;
USE veronicasoptica;
CREATE TABLE PROVEEDOR(
    ID_PROVEEDOR INT(100) NOT NULL AUTO_INCREMENT,
    PRIMARY KEY (ID_PROVEEDOR),
    NOMBRE VARCHAR(20) NOT NULL,
    CALLE VARCHAR(30),
    NUMERO INT(3),
    PUERTA VARCHAR(2),
    CP INT(5),
    PAIS VARCHAR(10),
    TELEFONO INT(9),
    FAX INT(9),
    CIF VARCHAR(9)
    );

CREATE TABLE COMPRAS(
    ID_PROVEEDOR INT(100) NOT NULL AUTO_INCREMENT,
    FOREIGN KEY (ID_PROVEEDOR) REFERENCES PROVEEDOR (ID_PROVEEDOR),
    MARCA VARCHAR(20)
);
