CREATE TABLE GAFAS(
    MARCA VARCHAR(20),
    GRADUACION_DERECHA DECIMAL(2,2),
    GRADUACION_IZQUIERDA DECIMAL(2,2),
    TIPO_DE_MONTURA VARCHAR(10),
    COLOR_MONTURA VARCHAR(10),
    COLOR_VIDRIOS VARCHAR(10),
    PRECIO DECIMAL(3,2)
);
CREATE TABLE CLIENTES (
    ID_CLIENTE INT(100) NOT NULL AUTO_INCREMENT,
    PRIMARY KEY (ID_CLIENTE),
    NOMBRE VARCHAR(10),
    CP INT(5),
    TELEFONO INT(9),
    MAIL VARCHAR(20),
    FECHA_REGISTRO TIMESTAMP(6),
    RECOMENDADO_POR INT(100),
    FOREIGN KEY (RECOMENDADO_POR) REFERENCES CLIENTES (ID_CLIENTE) 
    );
CREATE TABLE VENTAS(
    TRABAJADOR VARCHAR(10),
    FECHA_VENTA TIMESTAMP(6),
    ID_CLIENTE INT(100) NOT NULL AUTO_INCREMENT,
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTES (ID_CLIENTE)
);

